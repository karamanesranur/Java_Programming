package Java_Programming_Muhtar.day002_EscapeSequences;

public class EScapeSequences {


    public static void main(String[] args) {
        System.out.println("Hello students\nToday we have Java class");
        System.out.println("-----------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Programming Language");

        System.out.println("\tJava Programming Language");

        System.out.println("\\");
        System.out.println("\"");
        System.out.println("-----------------------------------");

        System.out.println("My favorite Tv show is \"The Walking Dead\"");

    }
}
/*
1.\n:New Line
2.\t:Paragraph space
3.\\:Black slash
4.\":Double quote
 */
