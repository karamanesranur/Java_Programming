package Java_Programming_Muhtar.day004_Concatenation;

public class Concatenation_vs_Addition {
    public static void main(String[] args) {

        System.out.println("32" + 1 ); // "321"

        System.out.println(7 + "1"); //"71"

        System.out.println("13" + "2"); //"132" //14.5 [this is concatenation]

        System.out.println("------------------------------------");

        System.out.println(12+2.5); //14.5 [this is additional]
        System.out.println("------------------------------------");






    }
}
