public class OperatorPrecedence {
    public static void main(String[] args) {

        System.out.println( (5 + 2) * 3);

        System.out.println(-5+2);
        System.out.println(10 > 9 == 8 < 9);

        int a = 20;

        a += 10+2*3; // a += 16
                     // a = 36




    }
}
