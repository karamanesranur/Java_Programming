package Java_Programming_Muhtar.day006_IfStatements;

public class PogNegZero {
    public static void main(String[] args) {

        int number = -20;


        if(number > 0 ){
            System.out.println("Positive");

        }
        if(number == 0 ){
            System.out.println("Zero");

        }

        if(number < 0 ){
            System.out.println("Negative");

        }
    }
}
